package classes;

public class Cont {
    private int nrCont;
    private double suma;

    public Cont(int nrCont, double suma) {
        this.nrCont = nrCont;
        this.suma = suma;
    }

    public int getNrCont() {
        return nrCont;
    }

    public void setNrCont(int nrCont) {
        this.nrCont = nrCont;
    }

    public double getSuma() {
        return suma;
    }

    public void setSuma(double suma) {
        this.suma = suma;
    }
}
